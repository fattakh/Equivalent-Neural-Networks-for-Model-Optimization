class CompressibleNeuralNetwork:
    """Neural network with equivalence transformation capabilities"""

    def __init__(self, layer_sizes, activation='relu', name="Network"):
        self.layer_sizes = layer_sizes
        self.activation = activation
        self.name = name
        self.weights = []
        self.biases = []

        # Initialize parameters
        for i in range(len(layer_sizes) - 1):
            # He initialization for ReLU
            std = np.sqrt(2.0 / layer_sizes[i])
            w = np.random.randn(layer_sizes[i], layer_sizes[i+1]) * std
            b = np.zeros((1, layer_sizes[i+1]))
            self.weights.append(w)
            self.biases.append(b)

        # Store training history
        self.loss_history = []
        self.accuracy_history = []

    def activation_function(self, x, derivative=False):
        """ReLU activation and its derivative"""
        if derivative:
            return (x > 0).astype(float)
        return np.maximum(0, x)

    def softmax(self, x):
        """Softmax for output layer"""
        exp_x = np.exp(x - np.max(x, axis=1, keepdims=True))
        return exp_x / np.sum(exp_x, axis=1, keepdims=True)

    def forward(self, X, cache_activations=False):
        """Forward propagation"""
        activations = [X]
        z_values = []

        # Hidden layers with ReLU
        for i in range(len(self.weights) - 1):
            z = np.dot(activations[-1], self.weights[i]) + self.biases[i]
            z_values.append(z)
            a = self.activation_function(z)
            activations.append(a)

        # Output layer (linear before softmax)
        z_final = np.dot(activations[-1], self.weights[-1]) + self.biases[-1]
        z_values.append(z_final)
        a_final = self.softmax(z_final)
        activations.append(a_final)

        if cache_activations:
            self.cached_activations = activations
            self.cached_z_values = z_values

        return a_final

    def backward(self, X, y, learning_rate):
        """Backward propagation"""
        m = X.shape[0]
        predictions = self.forward(X, cache_activations=True)

        # Output layer gradient (cross-entropy with softmax)
        dz = self.cached_activations[-1] - y

        # Gradients for output layer
        dW = [None] * len(self.weights)
        db = [None] * len(self.biases)

        dW[-1] = np.dot(self.cached_activations[-2].T, dz) / m
        db[-1] = np.sum(dz, axis=0, keepdims=True) / m

        # Backpropagate through hidden layers
        for i in range(len(self.weights) - 2, -1, -1):
            da = np.dot(dz, self.weights[i+1].T)
            dz = da * self.activation_function(self.cached_z_values[i], derivative=True)
            dW[i] = np.dot(self.cached_activations[i].T, dz) / m
            db[i] = np.sum(dz, axis=0, keepdims=True) / m

        # Update parameters
        for i in range(len(self.weights)):
            self.weights[i] -= learning_rate * dW[i]
            self.biases[i] -= learning_rate * db[i]

        # Calculate loss
        loss = -np.mean(np.sum(y * np.log(predictions + 1e-8), axis=1))
        return loss

    def train(self, X, y, epochs, learning_rate, X_val=None, y_val=None, verbose=True):
        """Train the network"""
        for epoch in range(epochs):
            loss = self.backward(X, y, learning_rate)
            self.loss_history.append(loss)

            # Calculate accuracy
            train_pred = np.argmax(self.forward(X), axis=1)
            train_acc = np.mean(train_pred == np.argmax(y, axis=1))
            self.accuracy_history.append(train_acc)

            if verbose and (epoch % 50 == 0 or epoch == epochs-1):
                val_msg = ""
                if X_val is not None:
                    val_pred = np.argmax(self.forward(X_val), axis=1)
                    val_acc = np.mean(val_pred == np.argmax(y_val, axis=1))
                    val_msg = f", Val Acc: {val_acc:.4f}"
                print(f"Epoch {epoch:3d}/{epochs} - Loss: {loss:.4f}, Train Acc: {train_acc:.4f}{val_msg}")

        return self.loss_history, self.accuracy_history

    def predict(self, X):
        """Make predictions"""
        probs = self.forward(X)
        return np.argmax(probs, axis=1)

    def get_size_info(self):
        """Get network size information"""
        total_params = 0
        for w in self.weights:
            total_params += w.size
        for b in self.biases:
            total_params += b.size

        return {
            'layers': self.layer_sizes,
            'total_params': total_params,
            'weights_shapes': [w.shape for w in self.weights]
        }

    def get_compression_ratio(self, compressed_network):
        """Calculate compression ratio"""
        original_params = sum(w.size for w in self.weights) + sum(b.size for b in self.biases)
        compressed_params = sum(w.size for w in compressed_network.weights) + sum(b.size for b in compressed_network.biases)
        return original_params / compressed_params


