class EquivalenceTransformer:
    """Apply equivalence transformations to compress networks"""

    @staticmethod
    def prune_dead_neurons(network, X_sample, threshold=0.01):
        """
        Remove neurons that rarely activate (dead neurons)
        This creates an equivalent but smaller network
        """
        # Forward pass to get activations
        network.forward(X_sample, cache_activations=True)

        # Check activation rates for each hidden layer
        new_weights = []
        new_biases = []

        current_weights = network.weights.copy()
        current_biases = network.biases.copy()

        for layer_idx in range(len(network.weights) - 1):
            # Get activations for this layer
            activations = network.cached_activations[layer_idx + 1]  # +1 because input is index 0

            # Calculate activation rate (how often each neuron fires)
            activation_rate = np.mean(activations > 0, axis=0)

            # Identify dead neurons (activation rate below threshold)
            dead_neurons = activation_rate < threshold

            if np.any(dead_neurons):
                print(f"  Layer {layer_idx + 1}: Removing {np.sum(dead_neurons)} dead neurons")

                # Keep only alive neurons
                alive_neurons = ~dead_neurons

                # Update weights for current layer (input to hidden)
                new_weights.append(current_weights[layer_idx][:, alive_neurons])
                new_biases.append(current_biases[layer_idx][:, alive_neurons])

                # Update weights for next layer (hidden to next)
                if layer_idx + 1 < len(current_weights):
                    new_weights.append(current_weights[layer_idx + 1][alive_neurons, :])
                    new_biases.append(current_biases[layer_idx + 1])
            else:
                # Keep original
                new_weights.append(current_weights[layer_idx])
                new_biases.append(current_biases[layer_idx])

        # Add output layer if not already added
        if len(new_weights) < len(current_weights):
            new_weights.append(current_weights[-1])
            new_biases.append(current_biases[-1])

        # Create compressed network
        compressed = CompressibleNeuralNetwork([network.layer_sizes[0]] +
                                               [w.shape[1] for w in new_weights[:-1]] +
                                               [network.layer_sizes[-1]],
                                               name=f"{network.name}_compressed")
        compressed.weights = new_weights
        compressed.biases = new_biases

        return compressed

    @staticmethod
    def merge_redundant_neurons(network, X_sample, similarity_threshold=0.95):
        """
        Merge similar neurons (neurons with nearly identical weights)
        This creates an equivalent network with fewer neurons
        """
        if len(network.weights) < 2:
            return network

        # Analyze first hidden layer weights
        weights_layer1 = network.weights[0]

        # Calculate cosine similarity between neurons
        from sklearn.metrics.pairwise import cosine_similarity

        # Normalize weights for comparison
        norms = np.linalg.norm(weights_layer1, axis=0, keepdims=True)
        norms[norms == 0] = 1
        normalized_weights = weights_layer1 / norms

        similarity = cosine_similarity(normalized_weights.T)

        # Find groups of similar neurons
        merged = set()
        merge_groups = []

        for i in range(similarity.shape[0]):
            if i in merged:
                continue

            similar = np.where(similarity[i] > similarity_threshold)[0]
            if len(similar) > 1:
                merge_groups.append(similar)
                merged.update(similar)

        if not merge_groups:
            print("  No redundant neurons found")
            return network

        print(f"  Found {len(merge_groups)} groups of redundant neurons")

        # Create merged network
        new_weights = []
        new_biases = []

        # Merge first layer
        merged_weights = []
        merged_biases = []
        used_indices = set()

        for group in merge_groups:
            # Average weights of redundant neurons
            merged_weight = np.mean(weights_layer1[:, group], axis=1, keepdims=True)
            merged_bias = np.mean(network.biases[0][group])
            merged_weights.append(merged_weight)
            merged_biases.append(merged_bias)
            used_indices.update(group)

        # Keep non-redundant neurons
        for i in range(weights_layer1.shape[1]):
            if i not in used_indices:
                merged_weights.append(weights_layer1[:, i:i+1])
                merged_biases.append(network.biases[0][i])

        new_weights.append(np.hstack(merged_weights))
        new_biases.append(np.array(merged_biases).reshape(1, -1))

        # Adjust next layer weights
        old_to_new_mapping = {}
        new_idx = 0
        for group in merge_groups:
            for old_idx in group:
                old_to_new_mapping[old_idx] = new_idx
            new_idx += 1
        for i in range(weights_layer1.shape[1]):
            if i not in used_indices:
                old_to_new_mapping[i] = new_idx
                new_idx += 1

        # Transform next layer weights
        old_weights_layer2 = network.weights[1]
        new_weights_layer2 = np.zeros((new_idx, old_weights_layer2.shape[1]))

        for old_idx, new_idx in old_to_new_mapping.items():
            if old_idx < len(merge_groups):
                # For merged neurons, average the connections
                group = merge_groups[old_idx]
                new_weights_layer2[new_idx] = np.mean(old_weights_layer2[group], axis=0)
            else:
                new_weights_layer2[new_idx] = old_weights_layer2[old_idx]

        new_weights.append(new_weights_layer2)
        new_biases.append(network.biases[1])

        # Create compressed network
        compressed = CompressibleNeuralNetwork([network.layer_sizes[0],
                                               new_weights[0].shape[1],
                                               network.layer_sizes[-1]],
                                               name=f"{network.name}_merged")
        compressed.weights = new_weights
        compressed.biases = new_biases

        return compressed

    @staticmethod
    def quantize_weights(network, bits=8):
        """
        Quantize weights to reduce memory usage
        Note: This creates an approximate equivalent network
        """
        quantized_weights = []
        quantized_biases = []

        for w in network.weights:
            # Find min and max
            w_min, w_max = w.min(), w.max()

            # Quantize
            if bits == 8:
                scale = (w_max - w_min) / 255
                w_quantized = np.round((w - w_min) / scale).astype(np.uint8)
                w_dequantized = w_quantized * scale + w_min
                quantized_weights.append(w_dequantized)
            else:
                quantized_weights.append(w)

        for b in network.biases:
            quantized_biases.append(b)

        compressed = CompressibleNeuralNetwork(network.layer_sizes, name=f"{network.name}_quantized")
        compressed.weights = quantized_weights
        compressed.biases = quantized_biases

        return compressed


